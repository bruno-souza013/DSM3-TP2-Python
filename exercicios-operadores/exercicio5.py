#5 - resultados da adição, subtração, multiplicação e divisão entre dois números
n1 = float(input("Digite o primeiro número: "))
n2 = float(input("Digite o segundo número: "))
soma = n1 + n2
sub = n1 - n2
mult = n1 * n2
div = n1 / n2
print(f"O resultado da soma: {soma:.2f}")
print(f"O resultado da subtração: {sub:.2f}")
print(f"O resultado da multiplicação: {mult:.2f}")
print(f"O resultado da divisão: {div:.2f}")
print()